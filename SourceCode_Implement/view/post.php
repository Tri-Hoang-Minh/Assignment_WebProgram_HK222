                            <h1 class="entry-title">Cách trồng rau Bắp Cải tại nhà từ A-Z cho bắp to đẹp ít sâu bệnh
                            </h1>
                            <div class="entry-content">
                                </p>
                                <p class="entry-summary mtb-15 text" itemprop="description">Ở miền Bắc và các vùng cao
                                    nguyên thích hợp để trồng bắp cải quanh năm, bởi vì những nơi đó có khí hậu mát lạnh
                                    hoặc một mùa Đông Xuân. Mà loại cây này lại ưa sống ở nơi nhiệt độ thấp, có khả năng
                                    chịu hạn rất tốt.</p>
                                </p>
                                <div class="entry-content mtb-15 text clearfix" itemprop="articleBody">
                                    <p>Tuy nhiên, nếu trồng với lượng nhỏ trong chậu, thùng xốp trên ban công hay sân
                                        thượng… thì ở đâu bạn vẫn có thể thử được. Vừa là thú vui, giúp giảm căng thẳng,
                                        mà biết đâu thành quả lại vượt ngoài mong đợi.</p>
                                    <p>Trong bài viết này, mình sẽ hướng dẫn chi tiết về thời vụ, cách trồng, chăm sóc
                                        và xử lý sâu bệnh cho bắp cải để bạn có thể tự tin làm tại nhà.</p>
                                    <h2><span id="1_Thoi_vu_trong_bap_cai">1. Thời vụ trồng bắp cải</span></h2>
                                    <p><strong>Vụ sớm</strong>: Bắt đầu gieo hạt vào cuối tháng 7 đến đầu tháng 8, trồng
                                        vào cuối tháng 8 tới trong tháng 9 và thu hoạch vào tháng 11 đến tháng 12.</p>
                                    <p><strong>Vụ chính</strong>: Gieo hạt vào tháng 9 đến tháng 10, trồng vào giữa
                                        tháng 10 tới hết tháng 11 và thu hoạch vào tháng 1 đến tháng 2 năm sau.</p>
                                    <p><strong>Vụ muộn</strong>: Gieo hạt vào tháng 11, trồng giữa tháng 12 và thu hoạch
                                        vào tháng 2 đến tháng 3 năm sau.</p>
                                    <p>Thông thường sau khoảng 30-40 ngày gieo hạt là có thể mang ra trồng, lúc này cây
                                        con đã có từ 5-7 lá thật.</p>
                                    <p>Bắp cải thích hợp trồng ở điều kiện khí hậu mát lạnh, ở những vùng cao nguyên như
                                        Sapa, Đà Lạt có thể trồng quanh năm, còn ở miền Bắc trồng mỗi năm một vụ.</p>
                                    <p><img decoding="async" loading="lazy" class="aligncenter"
                                            title="Cách trồng bắp cải"
                                            src="./asset/img/posts/post1.jpg"
                                            alt="Cách trồng bắp cải" width="640" height="480"></p>
                                    <h2><span id="2_Chon_giong_bap_cai_de_trong">2. Chọn giống bắp cải để trồng</span>
                                    </h2>
                                    <p>Nước ta chủ yếu nhập hạt giống bắp cải từ Nhật Bản và Trung Quốc. Một số giống
                                        phổ biến được bà con lựa chọn nhiều sau đây:</p>
                                    <p><strong>Giống bắp cải Sapa:</strong></p>
                                    <p>Thời gian sinh trưởng 135-145 ngày tính từ gieo hạt, bắp cải có màu trắng (hơi
                                        ngả vàng), khối lượng trung bình 1.8-2.0kg, năng suất trung bình 20-27 tấn/ha.
                                    </p>
                                    <p><strong>Giống bắp cải CB26:</strong></p>
                                    <p>Thời gian sinh trưởng 75-90 ngày, bắp giòn, chất lượng tốt, trọng lượng trung
                                        bình 1.2-1.5kg, năng suất trung bình 35 tấn/ha.</p>
                                    <p>Chịu nhiệt độ cao khi vào cuốn, khả năng chống bệnh thối nhũn, héo rũ tốt hơn
                                        nhiều loại bắp cải khác.</p>
                                    <p><strong>Giống bắp cải tím (C- 05) (Red ball):</strong></p>
                                    <p>Thời gian sinh trưởng khoảng 75 ngày, bắp cải hình tròn, bắp chắc, lá mọc thẳng,
                                        khối lượng trung bình 1.2-1.4kg. Có khả năng chịu khí hậu ẩm, chịu va chạm tốt
                                        trong vận chuyển.</p>
                                    <p><strong>Giống bắp cải F1 GM78:</strong></p>
                                    <p>Bắp có hình cầu dẹt, cuộn chắc, lá gốc màu xanh, ăn ngọt và mềm; chịu nhiệt tốt,
                                        khối lượng trung bình 1.5-2.2kg, khả năng chống bệnh thối nhũn tốt.</p>
                                    <h2><span id="3_Cach_uom_hat_giong_bap_cai">3. Cách ươm hạt giống bắp cải</span>
                                    </h2>
                                    <p><strong>Bước 1</strong>: Đầu tiên là làm đất, công đoạn này rất quan trọng, đất
                                        phải thoát nước tốt, giữ ẩm tốt, độ pH từ 6-6.5 là thích hợp, phải làm kĩ cho
                                        tơi xốp.</p>
                                    <p>Nên bón vôi và phơi ải cẩn thận trước khi gieo trồng để phòng ngừa các loại sâu
                                        bệnh hại như kiến, dế, sâu xanh, sâu tơ…</p>
                                    <p>Vào mùa khô thì lên luống cao 15-20cm, rộng 90-100cm, rãnh 30-45cm. Vào mùa mưa
                                        thì lên luống cao hơn 20-25cm, rộng 90-100cm, rãnh 35-50cm.</p>
                                    <p><strong>Bước 2</strong>: Bón lót bằng hỗn hợp 1kg phân chuồng hoai mục + 15g supe
                                        lân + 8g kali cho mỗi m2.</p>
                                    <p><u>Chú ý</u>: Hỗn hợp phân bón lót phải trộn đều với đất, rồi mới rải đều lên mặt
                                        luống, sau đó tiếp tục lấy đất ở rãnh phủ lên trên dày 1.5-2cm.</p>
                                    <p><strong>Bước 3</strong>: Ngâm hạt giống trong 20 phút vào nước ấm 50 độ, sau đó
                                        tiếp tục ngâm 8-10 tiếng trong nước lạnh. Rồi mới mang gieo bằng cách giải đều
                                        lên mặt luống mật độ 1.5-2 g/m2.</p>
                                    <p><img decoding="async" class="aligncenter"
                                            src="./asset/img/posts/post1.jpg"
                                            alt="Hạt giống bắp cải" title="hat-giong-bap-cai-1"></p>
                                    <p><strong>Bước 4</strong>: Gieo xong dùng cào để cào nhẹ hoặc dùng tay xoa nhẹ trên
                                        mặt luống để đất được phủ đều hạt giống, sau đó phủ bằng lớp rạ dày 1-2cm, rồi
                                        tưới đẫm nước bằng ô doa.</p>
                                    <p>Tưới nước 1-2 lần/ngày trong vòng 3-5 ngày đầu, ngừng tưới 1-2 ngày khi hạt nảy
                                        mầm nhô khỏi mặt đất, sau đó mới tưới 2 ngày/lần.</p>
                                    <p>Có thể làm giàn che để phòng ngừa mưa lớn hoặc nắng gắt.</p>
                                    <p><strong>Bước 5</strong>: Khi cây bắp cải lên 1-2 lá mầm thì gỡ bỏ lớp rơm rạ, chú
                                        ý gỡ nhẹ nhàng, gỡ xong tưới nước để bộ rễ chắc hơn.</p>
                                    <p><strong>Bước 6</strong>: Nhổ bỏ cây không đúng giống, cây bệnh, sao cho mật độ
                                        3-4cm. Dùng phân chuồng ngâm ngấu, pha loãng để tưới thúc sau mỗi lần tỉa.</p>
                                    <p><u>Lưu ý</u>: Nên bón thúc cho những cây kém phát triển bằng phân đạm pha với
                                        nước tỷ lệ 0.1%, nhưng chỉ được bón tối đa 2 lần, 1 lần vào thời điểm cây ra 2-3
                                        lá thật, lần 2 cách lần 1 từ 7-10 ngày.</p>
                                    <h2><span id="4_Ky_thuat_trong_bap_cai">4. Kỹ thuật trồng bắp cải</span></h2>
                                    <p>Một cây bắp cải đạt chuẩn thì phải có dáng mập, lùn, đốt sít, phiến lá tròn. Khi
                                        cây đủ 5-7 lá thật (30-40 ngày) thì mới được nhổ trồng, trồng non quá cây sẽ dễ
                                        bị chột hoặc chết.</p>
                                    <p>Nên ngưng tưới nước 4-7 ngày trước khi trồng để cây dễ làm quen với điều kiện
                                        ruộng. Trước khi nhổ 4-5 tiếng cần tưới nước cho đất mềm, như vậy nhổ sẽ không
                                        bị đứt rễ và hỏng cây.</p>
                                    <p><img decoding="async" class="aligncenter"
                                            src="./asset/img/posts/post1.jpg"
                                            alt="Đất trồng bắp cải" title="dat-trong-bap-cai-1"></p>
                                    <p><strong>Bước 1</strong>: Làm đất tơi xốp, vào mùa mưa thì lên luống cao 20-25cm,
                                        rộng 1-1.2m, rãnh 35-50cm; còn mùa khô thì luống cao 15-20cm, rộng 1-1.2m, rãnh
                                        30-40cm. Mặt luống phải phẳng để tránh đọng nước những ngày mưa.</p>
                                    <p><strong>Bước 2</strong>: Cuốc hố, bón phân lót bằng phân chuồng hoai mục, khoảng
                                        cách hố 40-45cm vào vụ đông xuân hoặc 35-40cm vào vụ hè thu.</p>
                                    <p>Mật độ trồng bắp cải 25000-33000 cây/ha hoặc 900-1200 cây/sào. Khoảng cách cây có
                                        thể điều chỉnh tùy vào vụ, nếu Đông Xuân thì cây cách cây 40cm, hàng cách hàng
                                        45cm; nếu Hè Thu thì cây cách cây 35cm, hàng cách hàng 40cm.</p>
                                    <p>Nên trồng bắp cải vào chiều muộn hoặc buổi tối, sau khi trồng thì cần tưới nước
                                        ngay 2 lần/ngày vào sáng sớm và chiều mát cho tới khi cây hồi xanh thì giảm
                                        xuống còn 3-5 ngày/lần.</p>
                                    <h2><span id="5_Bon_phan_cho_cay_bap_cai">5. Bón phân cho cây bắp cải</span></h2>
                                    <p><img decoding="async" class="aligncenter"
                                            src="./asset/img/posts/post1.jpg"
                                            alt="Cách trồng rau bắp cải" title="cach-trong-rau-bap-cai-1"></p>
                                    <p><strong>Các loại phân bón dùng cho bắp cải phổ biến như sau:</strong></p>
                                    <ul>
                                        <li>Phân hữu cơ (phân chuồng hoai mục) phải được ủ kỹ lưỡng và trải qua xử lý.
                                        </li>
                                        <li>Phân hóa học như đạm ure (hàm lượng nguyên chất 46%), phân lân (nung chảy
                                            14-16%), phân kali clorua (hàm lượng nguyên chất 60%) hoặc supe lân
                                            (16-18%).</li>
                                        <li>Phân hữu cơ vi sinh, bón qua rễ, dùng thay thế lân hóa học và đạm tới 50%.
                                            Phân này giúp bắp cải khỏe mạnh, chịu sâu bệnh bệnh tốt, cải tạo đất hiệu
                                            quả và giảm lượng nitrat trong rau.</li>
                                        <li>Các loại phân bón qua lá có hiệu quả nhanh trong từ 5-7 ngày, dùng để cung
                                            cấp vitamin, kích thích cây phát triển mà không gây độc hại.</li>
                                    </ul>
                                    <p><strong>Tuy nhiên phải tuân thủ một số nguyên tắc sau:</strong></p>
                                    <ul>
                                        <li><u>Dùng đúng loại phân</u>: Bón lót dùng phân chuồng và lân; bón thúc dùng
                                            phân hữu cơ vi sinh, đạm và kali.</li>
                                        <li><u>Dùng đúng liều lượng</u>: Với phân đạm bón thúc lần 2 liều lượng 1kg/sào,
                                            lần 4 thì 1.5kg/sào. Với phân kali bón thúc lần 3 liều lượng 3-5kg/sào. Với
                                            phân hữu cơ vi sinh bón thúc lần 1 qua rễ liều lượng 30-40kg/sào.</li>
                                        <li><u>Chọn đúng thời điểm bón</u>: Với phân đạm bón thúc lần 2 sau 10-15 ngày
                                            trồng, lần 4 khi cây vào cuốn. Với phân kali bón thúc lần 3 khi cây sắp trải
                                            lá rộng (lá bàng).</li>
                                        <li><u>Bón đúng cách</u>: Với kali và đạm thì cần hòa vào nước để tưới vào gốc;
                                            với phân hữu cơ sinh học thì bón quanh gốc rồi lấp lại.</li>
                                        <li>Ngưng bón phân 21 ngày trước khi thu hoạch.</li>
                                    </ul>
                                    <h2><span id="6_Cham_soc_cay_bap_cai">6. Chăm sóc cây bắp cải</span></h2>
                                    <p><img decoding="async" class="aligncenter"
                                            src="./asset/img/posts/post1.jpg"
                                            alt="Chăm sóc cây bắp cải" title="cham-soc-cay-bap-cai-1"></p>
                                    <p><strong>6.1. Giai đoạn từ trồng đến hồi sinh</strong></p>
                                    <ul>
                                        <li>Tưới nước ngay sau khi trồng và duy trì cho tới khi cây hồi xanh. Khi gặp
                                            trời mưa hoặc tưới nhiều sẽ gây ra tình trạng váng đất, vì vậy cần tiến hành
                                            xới khi xuất hiện váng.</li>
                                        <li>Trong những ngày đầu trồng, dặm thêm nếu có cây chết. Xới rộng và sâu để đất
                                            tới xốp, đồng thời giúp ngừa cỏ dại.</li>
                                        <li>Tiến hành bón thúc lần 1 và 2.</li>
                                        <li>Nên sử dụng hệ thống vòi phun tự động để tưới hiệu quả và tiết kiệm thời
                                            gian.</li>
                                    </ul>
                                    <p><strong>6.2. Giai đoạn từ hồi sinh tới trải lá</strong></p>
                                    <p>Giai đoạn này bắp cải cần nhiều nước và dinh dưỡng, vì vậy bà con chú ý:</p>
                                    <ul>
                                        <li>Cứ 7-10 ngày tưới rãnh 1 lần, tưới phun mưa 2 lần/ngày vào mùa khô và 2
                                            ngày/lần vào mùa mưa.</li>
                                        <li>Hòa phân kali với nước để bón vào gốc cây.</li>
                                        <li>Tiến hành xới đất nông, hẹp và vun nhẹ vào gốc.</li>
                                    </ul>
                                    <p><strong>6.3. Giai đoạn trải lá đến vào cuốn</strong></p>
                                    <p>Đây là giai đoạn cây bắp cải cần rất nhiều nước và dinh dưỡng, vì vậy bà con chú
                                        ý:</p>
                                    <ul>
                                        <li>Cần duy trì độ ẩm cho đất bằng cách tưới phun mưa và tưới rãnh.</li>
                                        <li>Vun gốc cao để tăng diện tích tiếp xúc với dinh dưỡng, đồng thời hạn chế cỏ
                                            dại hút dinh dưỡng.</li>
                                        <li>Tiến hành bón thúc lần 3 bằng kali với liều lượng 3-5kg/sào.</li>
                                        <li>Thường xuyên theo dõi ruộng rau, kịp thời phát hiện sâu bệnh gây hại để kịp
                                            thời xử lý.</li>
                                    </ul>
                                    <p><strong>6.4. Giai đoạn cuốn tới thu hoạch</strong></p>
                                    <ul>
                                        <li>Hòa phân đạm với nước tưới với liều lượng 1.5kg/sào.</li>
                                    </ul>
                                    <p>Song song với việc bón thúc, cần thực hiện các đợt làm cỏ, vun gốc, xới xáo, tưới
                                        nước. Không tưới bằng nước ao tù, nước thải khi chưa được xử lý; nên dùng nước
                                        sông, nước giếng khoan.</p>
                                    <p>Trồng bắp cải vụ sớm nếu gặp nắng to, cần che chắn cho cây con trong 3-5 ngày
                                        đầu. Có thể tháo nước ngập rãnh khi cây trải lá rộng (lá bàng), nhưng khi đủ ẩm
                                        cần tháo bỏ nước ngay.</p>
                                    <h2><span id="7_Sau_benh_hai_bap_cai">7. Sâu bệnh hại bắp cải</span></h2>
                                    <p><img decoding="async" loading="lazy" class="aligncenter wp-image-7227 size-full"
                                            title="Sâu bệnh hại bắp cải"
                                            src="./asset/img/posts/post1.jpg"
                                            alt="Sâu bệnh hại bắp cải" width="640" height="480"></p>
                                    <p><strong>7.1. Các loại sâu hại</strong></p>
                                    <p>Các loại sâu hại đều có biện pháp phòng ngừa chung, đó là thu dọn tàn dư cây
                                        trong trong vườn khi thu hoạch xong. Cần cây sâu cuốc lật đất 10 – 15 cm, phơi
                                        ải 10 – 15 ngày trước khi trồng. Cũng có thể lên luống, rồi phủ ni lông toàn mặt
                                        luống 3-5 ngày để diệt ấu trùng bệnh.</p>
                                    <p>Cần bón phân cân đối và đầy đủ, nên dùng phân hữu cơ hoai mục. Ngoài ra, nên luân
                                        canh cây khác họ, trồng tập trung vào đúng thời vụ.</p>
                                    <p>Một số lại sâu gây hại phổ biến là: Sâu tơ, sâu xanh bướm trắng, bọ nhảy sọc cong
                                        vỏ lạc… Cách phòng trừ đối với từng loại như sau:</p>
                                    <p><span style="text-decoration: underline;">Sâu tơ:</span> Ở giai đoạn cây con, nếu
                                        mật độ sâu trên 20 con/m2 hoặc trên 30 con/m2 đối với cây lớn thì dùng các loại
                                        thuốc: Cyperkill 10EC, 25EC, 5EC; Oncol 20EC, Reasgant 1.8EC, 3.6EC; Pegasu
                                        500SC, Catex 1.8EC, 3.6E; Sokupi 0,36AS…</p>
                                    <p><span style="text-decoration: underline;">Sâu xanh bướm trắng:</span> Khi sâu non
                                        đạt mật độ trên 6 con/m2 dùng thuốc: Delfin WG (32BIU), Ratoin 10EC, Cymerin
                                        10EC, Biocin 16WP…</p>
                                    <p><span style="text-decoration: underline;">Bọ nhảy:</span> Khi mật độ đạt trên 20
